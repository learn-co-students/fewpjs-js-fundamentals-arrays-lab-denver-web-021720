const append = ["Milo", "Otis", "Garfield"], 
      prepend = ["Milo", "Otis", "Garfield"], 
      removeLast = ["Milo", "Otis", "Garfield"], 
      removeFirst = ["Milo", "Otis", "Garfield"];

append.push('Odie') //Adds 'Odie' to end of array.

prepend.unshift('Odie') //Adds 'Odie' to beginning of array

removeLast.pop('Garfield') //Removes 'Garfield' from end of array

removeFirst.shift('Milo') //Removes 'Milo' from beginning of array